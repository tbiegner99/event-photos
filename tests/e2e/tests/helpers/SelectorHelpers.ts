export const dataId = (id: string) => `[data-id="${id}"]`;
export const dataRole = (role: string) => `[data-role="${role}"]`;
export const role = (role: string) => `[role="${role}"]`;
export const dataAttr = (attr:string, value: string) => `[data-${attr}="${value}"]`;
export const className = (className: string) => `.${className}`;